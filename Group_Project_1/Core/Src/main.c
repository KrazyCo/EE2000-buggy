void move_backward(uint16_t speed) {
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, speed);
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, speed);
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_SET); // Left DIR = reverse
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_SET); // Right DIR = reverse
}
void turn_left(uint16_t speed) {
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0); // Left motor off
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, speed); // Right motor on
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_RESET); // Left DIR = forward
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET); // Right DIR = forward
}
void turn_right(uint16_t speed) {

	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, speed); // Left motor on
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0); // Right motor off
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_RESET); // Left DIR = forward
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET); // Right DIR = forward
}

